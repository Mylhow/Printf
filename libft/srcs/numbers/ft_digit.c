#include "includes/libft.h"

int	ft_digit(long num)
{
	int i;

	i = 0;
	if (num == 0)
		i++;
	if (num < 0)
	{
		(num < 0) ? num *= -1 : 0;
	}
	while (num > 0)
	{
		num /= 10;
		i++;
	}
	return (i);
}
